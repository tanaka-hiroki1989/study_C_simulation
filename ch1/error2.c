/*計算誤差の例題プログラム          　　    　*/
/*丸め誤差の例題です                          */

#include <stdio.h>

int main()
{
 double x=0.0;
 int i ;

 /*10進の0.1の値*/
 printf("%f\n",0.1) ;

 /*0.1を1000000回加える*/
 for(i=0;i<1000000;++i)
  x=x+0.1 ;/*0.1は2進数では循環小数*/

 /*結果を出力*/
 printf("%f\n",x) ;

return 0 ;
}
